import java.util.TreeMap;

public class TreeMaps {

    public void treeMapExercise(){
        TreeMap<Integer,String> treeMaps = new TreeMap<>();


        // tree map is a sorted hash table/ map
        treeMaps.put(100,"abcd");
        treeMaps.put(5,"a");
        treeMaps.put(1,"b");
        treeMaps.put(3,"c");
        treeMaps.put(101,"d");

        System.out.println("TreeMap Example"+treeMaps);
        System.out.println();



    }
}
